    <h1 class="page-title"><strong>Rules of Explaining</strong></h1>
  </header>
  <div class="page-body">
    <ol
      type="1"
      id="3a55b82a-e4e4-4db9-8ac4-c299de572c83"
      class="numbered-list"
      start="1"
    >
      <li><strong>Be Nice</strong></li>
    </ol>
    <p id="5e6cecbb-4b1f-454e-a32a-a4f56d3df0b6" class="">
      Stay respectful, polite, and friendly. ELI5 is for requesting help
      understanding complex concepts and sharing explanations
      <a
        href="https://www.reddit.com/r/explainlikeimfive/comments/j28z5/welcome/"
        >without fear of judgment</a
      >. Don&#x27;t insult people or their good intentions in a post, comment,
      or otherwise, even if a person seems rude or ill-informed. Remember the
      positive spirit of ELI5.
    </p>
    <p id="df8d0300-fb87-45f0-9ad3-5472cf5a39d7" class="">
      This includes plagiarism - acknowledge your sources! The use of bigoted
      slurs, directed at other users or not, is uncivil.
    </p>
    <ol
      type="1"
      id="28e2ed79-422d-4adf-ba13-86ccea5196b1"
      class="numbered-list"
      start="1"
    >
      <li>
        <strong>All submissions must seek objective explanations.</strong>
      </li>
    </ol>
    <p id="0c85ac3e-917e-4703-8ac4-8ab92e7a190d" class="">
      ELI5 is not meant for any questions that you may have. It is meant to
      simplify complex <em>concepts</em>. Notably, posts will be immediately
      removed that ask for:
    </p>
    <ul id="3878543f-26b0-4d72-a402-b6119ad1bb9d" class="bulleted-list">
      <li style="list-style-type: disc">
        Information about a specific issue (personal experience,
        legal/relationship questions, medical questions, etc.)
      </li>
    </ul>
    <ul id="7a1b60fd-34fb-46bd-9bcc-42436d01ee44" class="bulleted-list">
      <li style="list-style-type: disc">Subjective/speculative replies</li>
    </ul>
    <ul id="24b04ed1-cf37-47e3-b823-88d1cba31651" class="bulleted-list">
      <li style="list-style-type: disc">Straightforward answers/facts</li>
    </ul>
    <ul id="6374849b-4068-4446-8dbb-c2a1a4761ccb" class="bulleted-list">
      <li style="list-style-type: disc">Recent/current events</li>
    </ul>
    <ul id="2fdc4b97-3554-4344-878d-1b28eb85026d" class="bulleted-list">
      <li style="list-style-type: disc">Religion/Politics</li>
    </ul>
    <ul id="0f119959-d716-4098-b509-03b653c93cef" class="bulleted-list">
      <li style="list-style-type: disc">Hypotheticals</li>
    </ul>
    <ul id="080e5f1e-db87-4558-b544-8f7d705da22a" class="bulleted-list">
      <li style="list-style-type: disc">Reddit questions (itself/help).</li>
    </ul>
    <ul id="ff7f91c6-0a5b-46e5-84cf-e766a9e44bdb" class="bulleted-list">
      <li style="list-style-type: disc">Whole topic overviews</li>
    </ul>
    <ul id="d41f94f2-4c94-44a0-8f8b-9681b8ba68c5" class="bulleted-list">
      <li style="list-style-type: disc">
        Business/Group/Individual motivations
      </li>
    </ul>
    <p id="0da96506-c368-442f-a4bb-8064787eafff" class="">
      <strong>Top-level comments must be written explanations</strong>
    </p>
    <p id="f1606227-fd30-4cac-9108-97973d8a01e9" class="">
      Replies to OP must be written explanations or relevant follow-up
      questions. They may not be jokes, anecdotes, etc. Short/succinct answers
      are not explanations, even if factually correct.
    </p>
    <p id="54c1a0a4-bb47-4982-ac61-7967fe846d88" class="">
      Links to outside sources are allowed and encouraged but must be
      accompanied by an original explanation (not just quoted text) or summary.
      Links to relevant previous ELI5 posts or highly relevant other subreddits
      may be excepted.
    </p>
    <p id="af934835-0435-426a-9454-b47922ee662a" class="">
      No plagiarism. All quoted text must be cited. This list is not
      comprehensive.
    </p>
    <p id="04500c4e-8363-441a-a846-59c0ef288f13" class="" />
    <p id="e96c69db-e53f-494c-8304-e8c59f78f0c2" class="">
      <strong>Explain for laypeople (but not actual 5-year-olds)</strong>
    </p>
    <p id="002a9b12-7c5b-4b65-b70c-05ef2e6444b7" class="">
      Unless OP states otherwise assume no knowledge beyond a typical secondary
      education program. Avoid unexplained technical terms. Don&#x27;t
      condescend; &quot;like I&#x27;m five&quot; is a figure of speech, meaning
      &quot;keep it clear and simple.&quot;
    </p>
    <p id="5dbb7ce0-7c76-45a2-b1cb-64e1b3b6f3fb" class="" />
    <p id="013f781a-9ade-4d0e-9c52-77828b7bffcd" class="">
      <strong>ELI5 is for factual information, not opinions</strong>
    </p>
    <p id="dde3e88a-e60e-40e1-aa4f-c93c883e90cc" class="">
      All comments must explain, ask about, correct or address facts. Trying to
      change someone&#x27;s opinion or influence them with yours is not
      appropriate.
    </p>
    <p id="19593085-42ea-46a5-ad98-d28f305fb721" class="">
      If you are not able to submit, discuss or correct factual information
      without implicitly or explicitly involving your personal view on the
      matter, do not post.
    </p>
    <p id="6d2ec18a-b232-49eb-bb84-fd81da4d0c6e" class="" />
    <p id="7804b0d5-3f6a-4fe8-93b7-5da05e7bd997" class="">
      <strong>Loaded questions are not allowed.</strong>
    </p>
    <p id="e643f2f9-f972-460c-ad6f-32f18e64a9f8" class="">
      A loaded question presumes a controversial or not obviously true statement
      as fact. &quot;ELI5: Why was Obama the president when he wasn&#x27;t born
      in the US?&quot; is loaded. &quot;ELI5: The controversy about where Obama
      was born&quot; is a non-loaded way to ask about the same topic.
    </p>
    <p id="921a3aae-1c47-4b52-a26a-0374f73e84fa" class="">
      If your question boils down to: &quot;Why isn&#x27;t this thing I believe
      (or is self evidently true) the case?&quot; your question is loaded and
      belongs in another subreddit.
    </p>
    <p id="f6ccda19-997f-4333-b14b-baf7607cc2a6" class="" />
    <p id="334c0b37-99ab-4c62-a1c5-e1746f3f5723" class="">
      <strong>Search before posting; don&#x27;t repeat old posts.</strong>
    </p>
    <p id="d82e28dc-8ee1-4cad-9044-90dcad5efad4" class="">
      To avoid repeats, search keywords (&quot;storm&quot; for &quot;How do
      Thunderstorms Form?&quot;), not the whole title. If you found only
      explanations that weren&#x27;t clear on your point, explain that and state
      what you&#x27;re still trying to understand.
    </p>
    <p id="c891809f-9143-4cfd-9ee5-a0a651ed3cd0" class="">
      “Old posts” refers to questions older than six months. If there are
      unarchived posts on the topic, you must explain why those posts do not
      answer the question.
    </p>
    <p id="96002fb3-c7ec-40b6-83f0-10420541e9be" class="">
      You can read more about our reposts policy
      <a href="https://www.reddit.com/r/explainlikeimfive/wiki/reposts">here</a
      >.
    </p>
    <p id="421eb089-92e7-4e90-a696-ba4da7ee6d91" class="" />
    <p id="2314befb-0959-4725-bdf1-766b18234ef4" class="">
      <strong>Don&#x27;t guess</strong>
    </p>
    <p id="6e9d8776-def5-4f46-b85d-7fa0a50e1cc7" class="">
      If you don&#x27;t know how to explain something, you don&#x27;t need to
      reply. If you have a partly educated guess, only comment on the parts you
      are sure of.
    </p>
    <p id="ecde940a-b023-4e8c-8ea9-4c1c12f00908" class="" />
    <p id="a608c3b5-cb77-49f2-b344-5b137e290dbb" class="">
      <strong>Don&#x27;t try to trick the bot.</strong>
    </p>
    <p id="bdb60528-aa13-4890-a13a-55fdbb2dfc5d" class="">
      If the automatic moderator removes your post, read the whole message, it
      gives you, and follow its advice. It&#x27;s there to help, not to be
      evaded — evasion will result in a ban.
    </p>
    <p id="a38e0c4b-48c2-4fdd-bffb-d44d5339ad89" class="" />
    <p id="8a8e98e8-e8e9-4f8a-863f-d5b80e676ade" class="">
      <strong>Posts must begin with &quot;ELI5:&quot;</strong>
    </p>
    <p id="b278bc8c-b94d-4ae5-85a6-7b4d9bd455de" class="">
      This is meant to help identify ELI5 posts on the front page or
      <a href="https://www.reddit.com/r/all">/r/all</a> or any other collection.
    </p>
